package com.ifpb.teste;

public class Pessoa {

	String nome;
	int idade;
	String email;
	String telefone;
	String telefone2;
	String rua;
	int num;
	
	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getRua() {
		return rua;
	}

	public void setRua(String rua) {
		this.rua = rua;
	}

	public String getTelefone2() {
		return telefone2;
	}

	public void setTelefone2(String numero) {
		this.telefone2 = numero;
	}

	public String getTelefone() {
		return telefone;
	}

	public void setTelefone(String numero) {
		this.telefone = numero;
	}
	

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	

}
